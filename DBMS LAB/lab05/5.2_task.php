<?php
// Purpose: Converts a value to an integer.
$x = "15.8";
echo "Integer: " . intval($x) . "<br>"; // Output: 15

// Purpose: Converts a value to a floating-point number (float).
$x = "15.8";
echo "Float: " . floatval($x) . "<br>"; // Output: 15.8

// Purpose: Converts a value to a string.
$x = 100;
echo "String: " . strval($x) . "<br>"; // Output: "100"
?>
